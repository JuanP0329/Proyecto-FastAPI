### Comprendiendo los Type Hints en FastAPI###


my_string_variable = "Mi variable String"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

my_type_variable: str = "Mi tipo de variable String"
print(my_type_variable)
print(type(my_type_variable))

my_type_variable: str = 5
print(my_type_variable)
print(type(my_type_variable))
